public class Appointment {
    private int id;
    private int patient_id;
    private int doctor_id;
    private String symptom;
    private String background_disease;
    private String diagnose;
    private Date date_appoint;
    private int start;
    private int end;
    private String status;

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public int getStart() {
        return start;
    }

    public void setStart(int start) {
        this.start = start;
    }

    public int getEnd() {
        return end;
    }

    public void setEnd(int end) {
        this.end = end;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getPatient_id() {
        return patient_id;
    }

    public void setPatient_id(int patient_id) {
        this.patient_id = patient_id;
    }

    public int getDoctor_id() {
        return doctor_id;
    }

    public void setDoctor_id(int doctor_id) {
        this.doctor_id = doctor_id;
    }

    public String getSymptom() {
        return symptom;
    }

    public void setSymptom(String symptom) {
        this.symptom = symptom;
    }

    public String getBackground_disease() {
        return background_disease;
    }

    public void setBackground_disease(String background_disease) {
        this.background_disease = background_disease;
    }

    public String getDiagnose() {
        return diagnose;
    }

    public void setDiagnose(String diagnose) {
        this.diagnose = diagnose;
    }

    public Date getDate_appoint() {
        return date_appoint;
    }

    public void setDate_appoint(Date date_appoint) {
        this.date_appoint = date_appoint;
    } 
}
