    private void tbShowListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbShowListMouseClicked
        // TODO add your handling code here:
        
        int index = tbShowList.getSelectedRow();
        TableModel model = tbShowList.getModel();
        
        String freeschedule_id = model.getValueAt(index, 0).toString();
        
        txtID.setText(freeschedule_id);
        
        //SEARCH FOR FREE SCHEDULE ID
        
        FreeSchedule free = FunctionFreeSchedule.SearchForID(Integer.parseInt(freeschedule_id));
        
        //SET DOCTOR ID
        
        int indexTemp=-1;
        for (int i = 0; i < cbxDID.getItemCount();++i){
            if(cbxDID.getItemAt(i).equals(model.getValueAt(index, 1).toString())){
                indexTemp = i;
                break;
            }
        }
        cbxDID.setSelectedIndex(indexTemp);
        
        //SET DATE
        
        String[] dateList = {"Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday", "Sunday"};
        cbxDate.removeAllItems();
        for(int i=0;i<7;i++){
            cbxDate.addItem(dateList[i]);
        }
        
        ArrayList<FreeSchedule> listf = FunctionDoctor.GetAllFreeSchedule(free.getDid());
        Iterator<FreeSchedule> itrf = listf.iterator();
        while (itrf.hasNext()) {
            
            FreeSchedule freeObj = itrf.next();
            
            if(!freeObj.getDay_free().equals(free.getDay_free())){
                int check = FindPosition(freeObj.getDay_free());
                if(check != -1){
                    cbxDate.removeItemAt(check);
                }
            }
        }
        
        indexTemp=-1;
        for (int i = 0; i < cbxDate.getItemCount();++i){
            if(cbxDate.getItemAt(i).equals(free.getDay_free())){
                indexTemp = i;
            }
        }
        cbxDate.setSelectedIndex(indexTemp);
        
        //SET TIME START AND END
        
        int start = free.getTime_start();
        int finish = free.getTime_end();
        
        String tstart, tfinish;
        
        if(start < 10){
            tstart = new String("0"+start+":00");
        }else{
             tstart = new String(start+":00");
        }
        if(finish < 10){
            tfinish = new String("0"+finish+":00");
        }else{
             tfinish = new String(finish+":00");
        }
        
        cbxStart.removeAllItems();
        cbxFinish.removeAllItems();
        cbxStart.addItem(tstart);
        cbxFinish.addItem(tfinish);
        
        String[] listStart = {"07:00","08:00","09:00","10:00","11:00","13:00","14:00","15:00","16:00"};
        String[] listFinish = {"08:00","09:00","10:00","11:00","12:00","14:00","15:00","16:00","17:00"};
        for(int i =0; i<=8;i++){
            if(!listStart[i].equals(tstart))
                cbxStart.addItem(listStart[i]);
            if(!listFinish[i].equals(tfinish))
                cbxFinish.addItem(listFinish[i]);
        }
    }//GEN-LAST:event_tbShowListMouseClicked

