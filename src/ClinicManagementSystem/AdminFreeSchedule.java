    public void FindDoctorName(){
        String doctor_id =  (String) cbxDID.getSelectedItem();
        if(doctor_id != null && !doctor_id.equals("Choose DID")){
            Doctor doctor = FunctionDoctor.SearchForID(Integer.parseInt(doctor_id));
            txtDname.setText(doctor.getName());
        }else{
            txtDname.setText("");
        }
    }
    
    public int FindPosition(String date){
        int position= -1;
        for(int i =0; i < cbxDate.getItemCount(); i++){
            if(date.equals(cbxDate.getItemAt(i))){
                position = i;
                return position;
            }
        }
        return position;
    }
