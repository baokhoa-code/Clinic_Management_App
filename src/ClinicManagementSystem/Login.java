    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String type = new String((String)cbxType.getSelectedItem());
        String email = txtEmail.getText();
        String pass = txtPass.getText();
        if(type.equals("Admin")){
            Admin admin = null;
            admin = FunctionAdmin.Login(email,pass);
            if (admin == null)
                JOptionPane.showMessageDialog (null,"<html><b style=\"color:red\">Incorrect Email "
                        + "or Password</b></html>", "Message", JOptionPane.ERROR_MESSAGE);
            else {
                setVisible(false);
                new AdminHome(email).setVisible(true);
            }
        }
        if(type.equals("Doctor")){
            Doctor doctor = null;
            doctor = FunctionDoctor.Login(email,pass); 
            if (doctor == null)
                JOptionPane.showMessageDialog (null,"<html><b style=\"color:red\">Incorrect Email "
                        + "or Password</b></html>", "Message", JOptionPane.ERROR_MESSAGE);
            else {
                if (doctor.getStatus().equals("FALSE")) {
                    ImageIcon icon = new ImageIcon("src/Images/loading.png");
                    JOptionPane.showMessageDialog(null, "<html><b>Please, wait for Admin accept</b></html>", 
                            "Message", JOptionPane.INFORMATION_MESSAGE, icon);
                    Clear();       
                }
                if (doctor.getStatus().equals("TRUE")) {
                    setVisible(false);
                    new DoctorHome(doctor.getId()).setVisible(true);
                }
            }
        }
        if(type.equals("Patient")){
            Patient patient = null;
            patient = FunctionPatient.Login(email,pass);
            if (patient == null)
                JOptionPane.showMessageDialog (null,"<html><b style=\"color:red\">Incorrect Email "
                        + "or Password</b></html>", "Message", JOptionPane.ERROR_MESSAGE);
            else {
                if (patient.getStatus().equals("FALSE")) {
                    ImageIcon icon = new ImageIcon("src/Images/loading.png");
                    JOptionPane.showMessageDialog(null, "<html><b>Please, wait for Admin accept</b></html>", 
                            "Message", JOptionPane.INFORMATION_MESSAGE, icon);
                    Clear();       
                }
                if (patient.getStatus().equals("TRUE")) {
                    setVisible(false);
                    new PatientHome(patient.getId()).setVisible(true);
                }
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

