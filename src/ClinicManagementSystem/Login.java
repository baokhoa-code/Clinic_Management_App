    public void validateFields() {
        String email = txtEmail.getText();
        String password = txtPass.getText();
        if(email.matches(emailTemplate) && !password.equals(""))
            btnLogin.setEnabled (true);
        else
            btnLogin.setEnabled(false);     
    }
    
