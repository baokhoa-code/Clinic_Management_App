    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel)tbShowList.getModel();
        
        ArrayList<Bill> list = FunctionPatient.GetAllBill(patient_id);
        Iterator<Bill> itr = list.iterator();
        
        while (itr.hasNext()) {
            Bill billObj = itr.next();
            dtm.addRow(new Object[]{billObj.getId(), billObj.getAppointment_id(), billObj.getDay_created(), billObj.getTotal_price()});
        }
    }//GEN-LAST:event_formComponentShown

