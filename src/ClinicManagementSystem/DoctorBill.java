    private void btnCreateBillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateBillActionPerformed
        // TODO add your handling code here:
        String tgrandtotal, tappointmentid;
        
        tgrandtotal = txtGrandTotal.getText();
        tappointmentid = txtAppointmentID.getText();
        
        int grandtotal, appointmentid;
        
        grandtotal = Integer.parseInt(tgrandtotal);
        appointmentid = Integer.parseInt(tappointmentid);
        
        if(grandtotal != 0 && !tappointmentid.equals("")){
            Bill bill = new Bill();
            bill.setAppointment_id(appointmentid);
            bill.setTotal_price(tgrandtotal);
            
            FunctionBill.Add(bill);
            FunctionAppointment.ChangeStatus(appointmentid);
            
            int bill_id = FunctionBill.FindNearestID();

            for(int i = 0; i < tbBillDetail.getRowCount(); i++){
                int drug_id = Integer.valueOf(tbBillDetail.getValueAt(i, 0).toString());
                String name = tbBillDetail.getValueAt(i, 1).toString();
                String price = tbBillDetail.getValueAt(i, 2).toString();
                int quantity = Integer.valueOf(tbBillDetail.getValueAt(i, 3).toString());
                String total = tbBillDetail.getValueAt(i, 4).toString();
                
                BillDetail billdetail = new BillDetail();
                billdetail.setBid(bill_id);
                billdetail.setDid(drug_id);
                billdetail.setName(name);
                billdetail.setPrice(price);
                billdetail.setQuantity(quantity);
                billdetail.setTotal(total);
                FunctionBillDetail.Add(billdetail);
            }
            setVisible(false);
            new DoctorBill(doctor_id).setVisible(true);
        }else
            JOptionPane.showMessageDialog (null,"<html><b style=\"color:red\">You did not choose Appointment ID or any drug, please do it!</b></html>", "Message", JOptionPane.ERROR_MESSAGE);  

        
    }//GEN-LAST:event_btnCreateBillActionPerformed

