    private void btnAddToBillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddToBillActionPerformed
        // TODO add your handling code here:
        if(!txtDrugID.getText().equals("")){
            DefaultTableModel dtmBillDetail = (DefaultTableModel)tbBillDetail.getModel();
        
            dtmBillDetail.addRow(new Object[]{txtDrugID.getText(),txtName.getText(),txtPrice.getText(),jspQuantity.getValue().toString(), txtTotal.getText()});

            DefaultTableModel dtmDrug = (DefaultTableModel)tbShowDrugList.getModel();
            dtmDrug.removeRow(selectRow);
            selectRow = -1;

            int total,grandTotal;

            total = Integer.parseInt(txtTotal.getText());
            grandTotal = Integer.parseInt(txtGrandTotal.getText());
            grandTotal += total;
            txtGrandTotal.setText(String.valueOf(grandTotal));

            txtDrugID.setText("");
            txtName.setText("");
            txtPrice.setText("0");
            jspQuantity.setValue(0);
            selectRow = -1;
        }else{
            if(txtAppointmentID.getText().equals(""))
               JOptionPane.showMessageDialog (null,"<html><b style=\"color:red\">You did not choose an Appoinment, please do it!</b></html>", "Message", JOptionPane.ERROR_MESSAGE);  
            else
                JOptionPane.showMessageDialog (null,"<html><b style=\"color:red\">You did not choose Drug from Drug List, please do it!</b></html>", "Message", JOptionPane.ERROR_MESSAGE);  
        }
        
    }//GEN-LAST:event_btnAddToBillActionPerformed

