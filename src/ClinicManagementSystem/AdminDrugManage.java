    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel)tbShowList.getModel();
        ArrayList<Drug> list = FunctionDrug.GetAllRecords();
        Iterator<Drug> itr = list.iterator();
        while (itr.hasNext()) {
            Drug drugObj = itr.next();
            dtm.addRow(new Object[]{drugObj.getId(), drugObj.getName(), drugObj.getCompany_name(), drugObj.getMfg(), drugObj.getExp(), drugObj.getPrice()});
        }
    }//GEN-LAST:event_formComponentShown

    private void tbShowListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbShowListMouseClicked
        // TODO add your handling code here:
        int index = tbShowList.getSelectedRow();
        TableModel model = tbShowList.getModel();
        String drug_id = model.getValueAt(index, 0).toString();
        txtID.setText(drug_id);
        Drug drug = FunctionDrug.SearchForID(Integer.parseInt(drug_id));
        
        txtName.setText(drug.getName());
        txtComName.setText(drug.getCompany_name());
        txtComposition.setText(drug.getComposition());
        jdcManuDate.setDate(drug.getMfg());
        jdcExp.setDate(drug.getExp());
        txtPrice.setText(drug.getPrice());
        btnDelete.setEnabled(true);
    }//GEN-LAST:event_tbShowListMouseClicked

