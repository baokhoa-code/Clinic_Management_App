    public void validateFields() {
        String drug_id = txtID.getText();
        String name = txtName.getText();
        String comname = txtComName.getText();
        String composition = txtComposition.getText();
        Date manu = jdcManuDate.getDate();
        Date exp = jdcExp.getDate();
        if(drug_id.equals("")){
            btnDelete.setEnabled(false);
            btnUpdate.setEnabled(false); 
            if(!name.equals("") && manu != null && exp != null && !comname.equals("") && !composition.equals("")){
                btnAdd.setEnabled (true);              
            }
            else{
                btnAdd.setEnabled (false);
            }
        }
        else {
            btnAdd.setEnabled (false);  
            if(!name.equals("") && manu != null && exp != null && !comname.equals("") && !composition.equals("")){                                              
                btnUpdate.setEnabled(true);
            }
            else{
                btnUpdate.setEnabled(false);
            }               
        }            
    }

