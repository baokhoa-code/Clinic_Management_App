    private void jspQuantityStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jspQuantityStateChanged
        // TODO add your handling code here:
        int quantity = (Integer)jspQuantity.getValue();
        if (quantity <=0) {
            jspQuantity.setValue(0);
            quantity = 0;
        }
        txtDrugTotal.setText(String.valueOf(quantity * Integer.parseInt(txtDrugPrice.getText())));
        btnDelete.setEnabled(false);
    }//GEN-LAST:event_jspQuantityStateChanged

