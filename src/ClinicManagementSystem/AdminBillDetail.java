    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel)tbShowList.getModel();
        ArrayList<BillDetail> list = FunctionBillDetail.GetAllRecordsForBillID(bill_id);
        Iterator<BillDetail> itr = list.iterator();
        while (itr.hasNext()) {
            BillDetail billdetailObj = itr.next();
            dtm.addRow(new Object[]{ billdetailObj.getDid(), billdetailObj.getName(), billdetailObj.getPrice(), billdetailObj.getQuantity(), billdetailObj.getTotal()});
        }
    }//GEN-LAST:event_formComponentShown

    private void tbShowListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbShowListMouseClicked
        // TODO add your handling code here:
        int index = tbShowList.getSelectedRow();
        TableModel model = tbShowList.getModel();
        String did = model.getValueAt(index, 0).toString();
        txtDrugID.setText(did);
        txtDrugName.setText(model.getValueAt(index, 1).toString());
        txtDrugPrice.setText(model.getValueAt(index, 2).toString());
        jspQuantity.setValue(Integer.parseInt(model.getValueAt(index, 3).toString()));
        txtDrugTotal.setText(model.getValueAt(index, 4).toString());
        btnDelete.setEnabled(true);
        btnUpdate.setEnabled(true);
    }//GEN-LAST:event_tbShowListMouseClicked

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int drug_id = Integer.parseInt(txtDrugID.getText());
        BillDetail temp = FunctionBillDetail.SearchForID(bill_id, drug_id);
        BillDetail billdetail = new BillDetail();
        billdetail.setBid(bill_id);
        billdetail.setDid(drug_id);
        billdetail.setName(txtDrugName.getText());
        billdetail.setPrice(txtDrugPrice.getText());
        billdetail.setQuantity(Integer.parseInt(String.valueOf(jspQuantity.getValue())));
        billdetail.setTotal(txtDrugTotal.getText());
        FunctionBillDetail.Update(billdetail);
        FunctionBill.UpdateTotal(bill_id, Integer.parseInt(txtTotal.getText()) - Integer.parseInt(temp.getTotal()) + Integer.parseInt(billdetail.getTotal()));
        setVisible (false);
        new AdminBillDetail(bill_id).setVisible(true);   
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int drug_id = Integer.parseInt(txtDrugID.getText());
        int total = Integer.parseInt(txtTotal.getText()) - Integer.parseInt(txtDrugTotal.getText());
        FunctionBill.UpdateTotal(bill_id, total);
        FunctionBillDetail.Delete(bill_id,drug_id);
        if(tbShowList.getRowCount()-1 <= 0){
            FunctionBill.DeleteNoMess(bill_id);
            setVisible (false);
            new AdminBill().setVisible(true);  
        }else{
            setVisible (false);
            new AdminBillDetail(bill_id).setVisible(true);  
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

