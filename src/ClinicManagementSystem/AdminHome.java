    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        Admin admin = new Admin();
        admin.setName(txtName.getText());
        admin.setDob(jdcDOB.getDate());
        admin.setGender((String) cbxGender.getSelectedItem());
        admin.setAddress(txtAddress.getText());
        admin.setPhone(txtPhone.getText());
        admin.setEmail(txtEmail.getText());
        admin.setPass(txtPass.getText());
        admin.setQuestion(txtQuest.getText());
        admin.setAnswer(txtAnswer.getText());
        if(admin.getName().equals(""))
            JOptionPane.showMessageDialog(null,"<html><b style=\"color:red\">"
                    + "Name can not be empty!</b></html>", "Message", JOptionPane.ERROR_MESSAGE);
        else if(!admin.getEmail().matches(emailTemplate))
            JOptionPane.showMessageDialog(null,"<html><b style=\"color:red\">"
                    + "Email is invalid!</b></html>", "Message", JOptionPane.ERROR_MESSAGE);
        else if(!(admin.getPhone().matches(phoneNumberTemplate) && admin.getPhone().length() == 10))
            JOptionPane.showMessageDialog(null,"<html><b style=\"color:red\">"
                    + "Phone is invalid!</b></html>", "Message", JOptionPane.ERROR_MESSAGE);
        else if(admin.getQuestion().equals("") || admin.getAnswer().equals(""))
            JOptionPane.showMessageDialog(null,"<html><b style=\"color:red\">"
                    + "Question and Answer can not be empty!</b></html>", "Message", JOptionPane.ERROR_MESSAGE);
        else if(admin.getPass().equals(""))
            JOptionPane.showMessageDialog(null,"<html><b style=\"color:red\">"
                    + "Password can not be empty!</b></html>", "Message", JOptionPane.ERROR_MESSAGE);
        else if(admin.getAddress().equals(""))
            JOptionPane.showMessageDialog(null,"<html><b style=\"color:red\">"
                    + "Address can not be empty!</b></html>", "Message", JOptionPane.ERROR_MESSAGE);
        else{
            FunctionAdmin.Update(admin);
            email = txtEmail.getText();
            setVisible (false);
            new AdminHome(email).setVisible(true);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

