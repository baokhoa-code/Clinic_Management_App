    public void Reset(){
        Admin admin = FunctionAdmin.SearchForID();
        txtName.setText(admin.getName());
        jdcDOB.setDate(admin.getDob());
        int indexTemp=-1;
        for (int i = 0; i < cbxGender.getItemCount();++i){
            if(cbxGender.getItemAt(i).equals(admin.getGender())){
                indexTemp = i;
                break;
            }
        }
        cbxGender.setSelectedIndex(indexTemp);
        txtAddress.setText(admin.getAddress());
        txtPhone.setText(admin.getPhone());
        txtEmail.setText(admin.getEmail());
        txtPass.setText(admin.getPass());
        txtQuest.setText(admin.getQuestion());
        txtAnswer.setText(admin.getAnswer());
    }
