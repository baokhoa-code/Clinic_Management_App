    private void tbShowListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbShowListMouseClicked
        // TODO add your handling code here:
        int index = tbShowList.getSelectedRow();
        TableModel model = tbShowList.getModel();
        String doctor_id = model.getValueAt(index, 0).toString();
        Doctor doctor = FunctionDoctor.SearchForID(Integer.parseInt(doctor_id));
        int x = JOptionPane.showConfirmDialog(null, "Do you want to accept this doctor account?", "Select", JOptionPane.YES_NO_OPTION);
        if (x == 0){
            doctor.setStatus("TRUE");
            FunctionDoctor.Update(doctor);
        }
        setVisible (false);
        new AdminDoctorCreateRequest().setVisible(true);
    }//GEN-LAST:event_tbShowListMouseClicked

