    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel)tbShowList.getModel();
        ArrayList<Doctor> list = FunctionDoctor.GetAllRequest();
        Iterator<Doctor> itr = list.iterator();
        while (itr.hasNext()) {
            Doctor doctorObj = itr.next();
            dtm.addRow(new Object[]{doctorObj.getId(), doctorObj.getName(), doctorObj.getAddress(), doctorObj.getPhone(), doctorObj.getEmail()});
        }
    }//GEN-LAST:event_formComponentShown

