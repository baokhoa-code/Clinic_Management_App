    private void tbShowListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbShowListMouseClicked
        // TODO add your handling code here:
        int index = tbShowList.getSelectedRow();
        TableModel model = tbShowList.getModel();
        String patient_id = model.getValueAt(index, 0).toString();
        txtID.setText(patient_id);
        Patient patient = FunctionPatient.SearchForID(Integer.parseInt(patient_id));
        
        txtName.setText(patient.getName());
        jdcDOB.setDate(patient.getDob());
        int indexTemp=-1;
        for (int i = 0; i < cbxGender.getItemCount();++i){
            if(cbxGender.getItemAt(i).equals(patient.getGender())){
                indexTemp = i;
                break;
            }
        }
        cbxGender.setSelectedIndex(indexTemp);
        txtAddress.setText(patient.getAddress());
        txtPhone.setText(patient.getPhone());
        txtEmail.setText(patient.getEmail());
        txtPass.setText(patient.getPass());
        txtQuest.setText(patient.getQuestion());
        txtAnswer.setText(patient.getAnswer());
        cbxBlood.removeAllItems();
        cbxBlood.addItem(patient.getBlood_group());
        String temp = new String(patient.getBlood_group());
        String[] blood= {"A+","A-","B+","B-","AB+","AB-","O+","O-"};
        for(int i =0;i<=7;i++){
            if(!blood[i].equals(temp)){
                cbxBlood.addItem(blood[i]);
            }
        }
        btnDelete.setEnabled(true);
    }//GEN-LAST:event_tbShowListMouseClicked

