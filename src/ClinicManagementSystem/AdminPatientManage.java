    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel)tbShowList.getModel();
        ArrayList<Patient> list = FunctionPatient.GetAllRecords();
        Iterator<Patient> itr = list.iterator();
        while (itr.hasNext()) {
            Patient patientObj = itr.next();
            dtm.addRow(new Object[]{patientObj.getId(), patientObj.getName(), patientObj.getAddress(), patientObj.getPhone(), patientObj.getEmail()});
        }
    }//GEN-LAST:event_formComponentShown

