    public void validateFields() {
        String id = txtID.getText();
        String name = txtName.getText();
        String gender = (String) cbxGender.getSelectedItem();
        String address = txtAddress.getText();
        String phone = txtPhone.getText();
        String email = txtEmail.getText();
        String pass = txtPass.getText();
        String quest = txtQuest.getText();
        String answer = txtAnswer.getText();
        String blood = (String) cbxBlood.getSelectedItem();
        Date date = jdcDOB.getDate(); 
        if(id.equals("")){
            btnDelete.setEnabled(false);
            btnUpdate.setEnabled(false); 
            if(!gender.equals("") && !name.equals("")&& email.matches(emailTemplate) && date != null && phone.matches(phoneNumberTemplate) && phone.length() == 10 && !address.equals("") && !pass.equals("") && !quest.equals("") && !answer.equals("")){
                btnAdd.setEnabled (true);
            }
            else{
                btnAdd.setEnabled (false);
            }
        }
        else {
            btnAdd.setEnabled (false);
            if(email.matches(emailTemplate) && date != null && phone.matches(phoneNumberTemplate) && phone.length() == 10 && !address.equals("") && !pass.equals("") && !quest.equals("") && !answer.equals("")){             
                btnUpdate.setEnabled(true);
            }
            else{
                btnUpdate.setEnabled(false);
            }               
        }            
    }
