    private void btnSignupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignupActionPerformed
        // TODO add your handling code here:
        String type = (String) cbxType.getSelectedItem();
        if(type.equals("Doctor")){
            Doctor doctor = new Doctor();
            doctor.setName(txtName.getText());
            doctor.setGender((String) cbxGender.getSelectedItem());
            doctor.setAddress(txtAddress.getText());
            doctor.setPhone(txtPhone.getText());
            doctor.setCertificate(txtCertificate.getText());
            doctor.setSpecialist(txtSpecialist.getText());
            doctor.setEmail(txtEmail.getText());
            doctor.setPass(txtPass.getText());
            doctor.setQuestion(txtQuest.getText());
            doctor.setAnswer(txtAnswer.getText());
            doctor.setStatus("FALSE");
            doctor.setDob(jdcDOB.getDate());
            FunctionDoctor.Register(doctor);
            Clear();
        }else{
            Patient patient = new Patient();
            patient.setName(txtName.getText());
            patient.setGender((String) cbxGender.getSelectedItem());
            patient.setAddress(txtAddress.getText());
            patient.setPhone(txtPhone.getText());
            patient.setBlood_group((String) cbxBlood.getSelectedItem());
            patient.setEmail(txtEmail.getText());
            patient.setPass(txtPass.getText());
            patient.setQuestion(txtQuest.getText());
            patient.setAnswer(txtAnswer.getText());
            patient.setStatus("FALSE");
            patient.setDob(jdcDOB.getDate());
            FunctionPatient.Register(patient);
            Clear();
        }
      
    }//GEN-LAST:event_btnSignupActionPerformed

