    public void validateFields() {
        String typ = (String) cbxType.getSelectedItem();
        String name = txtName.getText();
        String gender = (String) cbxGender.getSelectedItem();
        String address = txtAddress.getText();
        String phone = txtPhone.getText();
        String email = txtEmail.getText();
        String pass = txtPass.getText();
        String quest = txtQuest.getText();
        String answer = txtAnswer.getText();
        String certificate = txtCertificate.getText();
        String specialist = txtSpecialist.getText();
        String blood = (String) cbxBlood.getSelectedItem();
        Date date = jdcDOB.getDate(); 
        if(typ.equals("Doctor")&& !name.equals("")&& email.matches(emailTemplate) && date != null && phone.matches(phoneNumberTemplate) && phone.length() == 10 && !address.equals("") && !pass.equals("") && !quest.equals("") && !answer.equals("") && !certificate.equals("") && !specialist.equals(""))
            btnSignup.setEnabled (true);
        else {
            if(typ.equals("Patient")&& !name.equals("") && email.matches(emailTemplate) && date != null  && phone.matches(phoneNumberTemplate) && phone.length() == 10 && !address.equals("") && !pass.equals("") && !quest.equals("") && !answer.equals(""))
                btnSignup.setEnabled (true);
            else
                btnSignup.setEnabled(false);
        }
            
    }
