    public void Reset(){
        Doctor doctor = FunctionDoctor.SearchForID(doctor_id);
        txtName.setText(doctor.getName());
        jdcDOB.setDate(doctor.getDob());
        int indexTemp=-1;
        for (int i = 0; i < cbxGender.getItemCount();++i){
            if(cbxGender.getItemAt(i).equals(doctor.getGender())){
                indexTemp = i;
                break;
            }
        }
        cbxGender.setSelectedIndex(indexTemp);
        txtAddress.setText(doctor.getAddress());
        txtPhone.setText(doctor.getPhone());
        txtEmail.setText(doctor.getEmail());
        txtPass.setText(doctor.getPass());
        txtQuest.setText(doctor.getQuestion());
        txtAnswer.setText(doctor.getAnswer());
        txtCertificate.setText(doctor.getCertificate());
        txtSpecialist.setText(doctor.getSpecialist());
    }

