    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        Doctor doctor = new Doctor();
        doctor.setId(doctor_id);
        doctor.setName(txtName.getText());
        doctor.setDob(jdcDOB.getDate());
        doctor.setGender((String) cbxGender.getSelectedItem());
        doctor.setAddress(txtAddress.getText());
        doctor.setPhone(txtPhone.getText());
        doctor.setCertificate(txtCertificate.getText());
        doctor.setSpecialist(txtSpecialist.getText());
        doctor.setEmail(txtEmail.getText());
        doctor.setPass(txtPass.getText());
        doctor.setQuestion(txtQuest.getText());
        doctor.setAnswer(txtAnswer.getText());
        doctor.setStatus("TRUE");   
        if(doctor.getName().equals(""))
            JOptionPane.showMessageDialog(null,"<html><b style=\"color:red\">"
                    + "Name can not be empty!</b></html>", "Message", JOptionPane.ERROR_MESSAGE);
        else if(!doctor.getEmail().matches(emailTemplate))
            JOptionPane.showMessageDialog(null,"<html><b style=\"color:red\">"
                    + "Email is invalid!</b></html>", "Message", JOptionPane.ERROR_MESSAGE);
        else if(!(doctor.getPhone().matches(phoneNumberTemplate) && doctor.getPhone().length() == 10))
            JOptionPane.showMessageDialog(null,"<html><b style=\"color:red\">"
                    + "Phone is invalid!</b></html>", "Message", JOptionPane.ERROR_MESSAGE);
        else if(doctor.getQuestion().equals("") || doctor.getAnswer().equals(""))
            JOptionPane.showMessageDialog(null,"<html><b style=\"color:red\">"
                    + "Question and Answer can not be empty!</b></html>", "Message", JOptionPane.ERROR_MESSAGE);
        else if(doctor.getPass().equals(""))
            JOptionPane.showMessageDialog(null,"<html><b style=\"color:red\">"
                    + "Password can not be empty!</b></html>", "Message", JOptionPane.ERROR_MESSAGE);
        else if(doctor.getAddress().equals(""))
            JOptionPane.showMessageDialog(null,"<html><b style=\"color:red\">"
                    + "Address can not be empty!</b></html>", "Message", JOptionPane.ERROR_MESSAGE);
        else if(doctor.getCertificate().equals(""))
            JOptionPane.showMessageDialog(null,"<html><b style=\"color:red\">"
                    + "Certificate can not be empty!</b></html>", "Message", JOptionPane.ERROR_MESSAGE);
        else if(doctor.getSpecialist().equals(""))
            JOptionPane.showMessageDialog(null,"<html><b style=\"color:red\">"
                    + "sSpecialist can not be empty!</b></html>", "Message", JOptionPane.ERROR_MESSAGE);
        else{
            FunctionDoctor.Update(doctor);
            setVisible (false);
            new DoctorInformation(doctor_id).setVisible(true);
        }
        
    }//GEN-LAST:event_btnUpdateActionPerformed

