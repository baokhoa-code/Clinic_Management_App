    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        DefaultTableModel dtm = (DefaultTableModel)tbShowList.getModel();
        ArrayList<Bill> list = FunctionBill.GetAllRecords();
        Iterator<Bill> itr = list.iterator();
        while (itr.hasNext()) {
            Bill billObj = itr.next();
            dtm.addRow(new Object[]{billObj.getId(), billObj.getAppointment_id(), billObj.getDay_created(), billObj.getTotal_price()});
        }
    }//GEN-LAST:event_formComponentShown

    private void tbShowListMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbShowListMouseClicked
        // TODO add your handling code here:
        int index = tbShowList.getSelectedRow();
        TableModel model = tbShowList.getModel();
        Appointment appoint = FunctionAppointment.SearchForID(Integer.parseInt(model.getValueAt(index, 1).toString()));
        txtPID.setText(String.valueOf(appoint.getPatient_id()));
        txtDID.setText(String.valueOf(appoint.getDoctor_id()));
        int pid, did;
        pid = Integer.parseInt(txtPID.getText());
        did = Integer.parseInt(txtDID.getText());
        Patient patient = FunctionPatient.SearchForID(pid);
        Doctor doctor = FunctionDoctor.SearchForID(did);
        txtPname.setText(patient.getName());
        txtDname.setText(doctor.getName());
        txtSymptom.setText(appoint.getSymptom());
        txtDiagnose.setText(appoint.getDiagnose());
        btnDetail.setEnabled(true);
        btnDelete.setEnabled(true);
    }//GEN-LAST:event_tbShowListMouseClicked

