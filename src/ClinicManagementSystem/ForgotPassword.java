    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String type = (String) cbxType.getSelectedItem();
        String useranswer = txtAnswer.getText();
        String newPassword = txtNewPassword.getText();
        if(useranswer.equals(answer)) {
            String query = "UPDATE " + type + " SET PASS = '" + newPassword + "' WHERE EMAIL = '" + email +"'";
            DbOperations.setDataOrDelete(query, "Password Changed Complete");
            Clear();
        } 
        else {
            JOptionPane.showMessageDialog(null,"<html><b style=\"color:red\">Incorrect Answer</b></html>", "Message", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

