    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String type = (String) cbxType.getSelectedItem();
        String temp_email = txtEmail.getText ();
        try {
            ResultSet rs = DbOperations.getData("SELECT * FROM " + type 
                        + " WHERE EMAIL = '"+ temp_email +"'");
            while(rs.next()) {                                                
                txtQuestion.setText(rs.getString("Question"));
                answer = new String(rs.getString("ANSWER"));
                email = new String(rs.getString("EMAIL"));
            }
        }
        catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        if (answer == null)
            JOptionPane.showMessageDialog(null,"<html><b style=\"color:red\">"
                    + "Incorrect Email</b></html>", "Message", JOptionPane.ERROR_MESSAGE);
        else {
            btnSearch.setEnabled(false);
            txtEmail.setEditable(false);
            txtAnswer.setEditable(true);
            txtNewPassword.setEditable(true);
            validateFields();
        }
      
    }//GEN-LAST:event_btnSearchActionPerformed

